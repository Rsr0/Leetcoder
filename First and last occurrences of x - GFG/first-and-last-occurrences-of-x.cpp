class Solution
{
    public:
    vector<int> find(int arr[], int n , int x )
    {
        // code here
        auto low=lower_bound(arr, arr+n, x) - arr;
        auto high=upper_bound(arr, arr+n, x) - arr;
        // cout<<low<<"-"<<high<<endl;
        if(arr[low]!=x )     low=high=-1;
        else if(arr[high]!=x)    high--;
        return {low, high};
        
    }
};

